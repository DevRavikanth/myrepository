package controstatements;
public class PyramidExampleReverse {  
public static void main(String[] args) {  
for(int i=1;i<=5;i++){  
for(int j=5;j>=i;j--){  //j is compared with i
        System.out.print("* ");  
}  
System.out.println();//new line  
}  
}  
}  